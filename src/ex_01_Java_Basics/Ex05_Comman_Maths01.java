package ex_01_Java_Basics;

public class Ex05_Comman_Maths01 {
    public static void main(String[] args) {
        System.out.println(2+2);
        System.out.println(2-2);
        System.out.println(100/5);
        System.out.println(100*5);
        System.out.println(3/2); // Integer division output is always an integer not a decimal
        System.out.println(3.0/2); // Decimal division output is a decimal
        System.out.println(3%2); // Modulus operator gives the remainder of the division
    }
}
