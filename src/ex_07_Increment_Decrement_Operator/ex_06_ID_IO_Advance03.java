package ex_07_Increment_Decrement_Operator;

public class ex_06_ID_IO_Advance03 {
    public static void main(String[] args) {
        int a = 30;
        System.out.println(++a + ++a);
        System.out.println(a);

        // ++a value will increase by 1 = 31
        // Addition
        // ++a is 31 for another ++a it will again increase by 1 = 32
        // 20 + 32 = 62
        // value of a 32
    }
}
