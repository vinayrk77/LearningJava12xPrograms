package ex_02_Java_Basics_Part2;

public class ex_08_Numeric_Datatype {
    public static void main(String[] args) {
        byte b = 127;
        int i = 10;
        short s = 2;
        char c = 'v';
        long l = 8850139633l;
        long l1 = 8850139644l;
        // while using long use capital "L" or small "l" in the end
        float f = 20.30f;
        float f1 = 40.40F;
        // while using float use capital "F" or small "f" in the end
        Double D = 223.4455;
        System.out.println(f1);
        System.out.println(D);

    }
}
