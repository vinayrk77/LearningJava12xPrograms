package ex_09_Switch;

public class ex_09_Switch_Interview {
    public static void main(String[] args){
        char code = 'C';
        switch (code){
            default -> System.out.println("HELLOOO");
            case 'C' -> System.out.println("MOOO");
            case 2 -> System.out.println("ChOOO");
        }
    }
}
